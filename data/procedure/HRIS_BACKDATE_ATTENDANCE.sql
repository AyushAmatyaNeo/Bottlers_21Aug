create or replace PROCEDURE HRIS_BACKDATE_ATTENDANCE(
    P_ATTENDANCE_DT DATE,
    P_EMPLOYEE_ID HRIS_EMPLOYEES.EMPLOYEE_ID%TYPE,
    P_IN_TIME  TIMESTAMP,
    P_OUT_TIME TIMESTAMP)
AS
BEGIN
  INSERT
  INTO HRIS_ATTENDANCE
    (
      ATTENDANCE_DT,
      EMPLOYEE_ID,
      ATTENDANCE_TIME,
      ATTENDANCE_FROM
    )
    VALUES
    (
      P_ATTENDANCE_DT,
      P_EMPLOYEE_ID,
      P_IN_TIME,
      'SYSTEM'
    );
  INSERT
  INTO HRIS_ATTENDANCE
    (
      ATTENDANCE_DT,
      EMPLOYEE_ID,
      ATTENDANCE_TIME,
      ATTENDANCE_FROM
    )
    VALUES
    (
      P_ATTENDANCE_DT,
      P_EMPLOYEE_ID,
      P_OUT_TIME,
      'SYSTEM'
    );
  HRIS_REATTENDANCE(P_ATTENDANCE_DT,P_EMPLOYEE_ID);
END;