create or replace PROCEDURE HRIS_BACKDATE_ATTENDANCE(
    ATTENDANCE_DATE DATE,
    EMP_ID HRIS_EMPLOYEES.EMPLOYEE_ID%TYPE,
    IN_T  TIMESTAMP,
    OUT_T TIMESTAMP)
AS
BEGIN
  UPDATE HRIS_ATTENDANCE_DETAIL
  SET IN_TIME        =IN_T,
    OUT_TIME         = OUT_T
    WHERE EMPLOYEE_ID= EMP_ID AND ATTENDANCE_DT=ATTENDANCE_DATE;
END;