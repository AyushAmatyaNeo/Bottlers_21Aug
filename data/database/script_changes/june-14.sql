CREATE TABLE HRIS_COMPULSORY_OVERTIME
  (
    COMPULSORY_OVERTIME_ID NUMBER(7,0) PRIMARY KEY,
    EARLY_OVERTIME_HR      NUMBER(7,0),
    LATE_OVERTIME_HR       NUMBER(7,0),
    START_DATE             DATE NOT NULL,
    END_DATE               DATE,
    CREATED_BY             NUMBER(7,0),
    CREATED_DT             DATE,
    MODIFIED_BY            NUMBER(7,0),
    MODIFIED_DT            DATE,
    STATUS                 CHAR(1 BYTE) DEFAULT 'E' NOT NULL CHECK(STATUS IN ('E','D'))
  );
